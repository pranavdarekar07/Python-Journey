score = int(input("Enter you score : "))
if 90 <= score <= 100:
    print("Grade : A")
elif 80 <= score <= 89:
    print("Grade : B")
elif 70 <= score <= 79:
    print("Grade : C")
elif 60 <= score <= 69:
    print("Grade : D")
elif 50 <= score <= 59:
    print("Grade : E")
else : 
    print("Fail")
